    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!jTextField1.getText().equals("") && jPasswordField1.getPassword().length > 1){
            try {
                Profile profile = loginControler.checkUser(jTextField1.getText(), 
                        new String(jPasswordField1.getPassword()));
                if( profile == null){
                    jLabel3.setText("Перевірте коректність введених даних.");
                }
                else{
                    if(profile.getRole().getIdRole() == 1){
                        AdminWindow adminWindow = new AdminWindow(profile);
                        adminWindow.setVisible(true);
                    }
                    if(profile.getRole().getIdRole() == 2){
                        ManagerWindow managerWindow = new ManagerWindow(profile);
                        managerWindow.setVisible(true);
                    }
                    //вікно для певного користувача
                    this.dispose();
                }
                    } catch (SQLException ex) {
                Logger.getLogger(LoginView1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            jLabel3.setVisible(true);
            jLabel3.setText("Заповніть всі поля.");            
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
