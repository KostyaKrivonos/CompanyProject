    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int indexRow = this.jTable1.getSelectedRow();
        Product product = productsList.get(indexRow);
        if (jCheckBox1.isSelected() && jCheckBox2.isSelected()
                && !jTextField1.getText().equals("") && !jTextField2.getText().equals("")) {
            product.setName(jTextField1.getText());
            product.setPrice(Double.parseDouble(jTextField2.getText()));
            productControler.updateProductNamePrice(product);
            this.tableModel.fireTableDataChanged();
            jDialog1.setVisible(false);
        }
        if (jCheckBox1.isSelected() && !jTextField1.getText().equals("")) {
            product.setName(jTextField1.getText());
            productControler.updateProductName(product);
            this.tableModel.fireTableDataChanged();
            jDialog1.setVisible(false);
        }
        if (jCheckBox2.isSelected() && !jTextField2.getText().equals("")) {
            product.setPrice(Double.parseDouble(jTextField2.getText()));
            productControler.updateProductPrice(product);
            this.tableModel.fireTableDataChanged();
            jDialog1.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Проблеми оновлення.", "Error", JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_jButton6ActionPerformed

