    private class ProductTableModel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return productsList.size();
        }

        @Override
        public int getColumnCount() {
            return 3;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            switch(columnIndex){
                case 0:{
                    return productsList.get(rowIndex).getId();
                }
                case 1:{
                    return productsList.get(rowIndex).getName();
                }
                case 2:{
                    return productsList.get(rowIndex).getPrice();
                }
                default : return null;
            }
        }
        
        @Override
        public String getColumnName(int g){
            if(g == 0)
                return "Id";
            if(g == 1)
                return "Product";
            else 
                return "Price";      
        }
        
        @Override
         public Class<?> getColumnClass(int i){
            if(i == 0)
                return Integer.class;
            if(i == 1)
                return String.class;
            else 
                return Double.class;
        }               
    }

