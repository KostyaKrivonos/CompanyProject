     private class CustomerTableModel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return customers1.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            switch(columnIndex){
                case 0:{
                    return customers1.get(rowIndex).getId();
                }
                case 1:{
                    return customers1.get(rowIndex).getName();
                }
                case 2:{
                    return customers1.get(rowIndex).getAdress();
                }
                case 3:{
                    return customers1.get(rowIndex).getPhone();
                }
                default : return null;
            }
        }
        @Override
        public String getColumnName(int g){
            if(g == 0)
                return "Id";
            if(g == 1)
                return "Name";
            if(g == 2)
                return "Adress";
            else 
                return "Phone";      
        }
        
        @Override
        public Class<?> getColumnClass(int i){
            if(i == 0)
                return Integer.class;
            if(i == 1)
                return String.class;
            if(i == 2)
                return String.class;
            else 
                return String.class;
        }
        
    }
